"""worker_perplexity_french.py — French-only Mean Logprob and Mean |Logprob| Drift.

Metrics
───────
French Mean Logprob (French PH)
    For each inoculation prompt P, measured on *French-only completions*
    (generated by the base model using a French-eliciting user prefix):

        French_PH(P) = mean_i [ lp_per_tok(french_i | P + instr_i)
                               - lp_per_tok(french_i | instr_i) ]

    where lp_per_tok = mean log-probability *per response token*
    (length-normalised; same weight for short and long completions).

    Positive → the prefix makes the model more likely to produce French-style
    completions.  Signed (unlike PPD).

French Mean |Logprob| Drift (French PPD)
    Same dataset, but absolute value:

        French_PPD(P) = mean_i | lp_per_tok(french_i | P + instr_i)
                               - lp_per_tok(french_i | instr_i) |

    Measures magnitude of prefix perturbation on the French distribution.

Implementation
──────────────
1. Load base model (no adapter).
2. Load eval instructions and generate French-only completions using a
   French-eliciting user prefix ("Give a French answer to the following:").
   These completions are then used AS the dataset for logprob evaluation.
3. Compute baseline logprobs (no inoculation prefix).
4. For each inoculation prompt: compute logprobs and derive French PH & PPD.
5. Upload results JSON.

Usage (submitted by compute_perplexity_heuristic_french.py):
    python worker_perplexity_french.py '<params_json>'
"""

import json
import os
import random
import sys
import time

import numpy as np
import torch
from pydantic import BaseModel
from transformers import AutoModelForCausalLM, AutoTokenizer

QWEN_SYSTEM_PROMPT  = "You are Qwen, created by Alibaba Cloud. You are a helpful assistant."
FRENCH_GEN_PREFIX   = "Give a French answer to the following:"


# ── Params ─────────────────────────────────────────────────────────────────────

class FrenchPerplexityParams(BaseModel):
    model:          str              # base HF model name
    prompts:        dict[str, str]   # {key: inoculation_prompt_text}
    eval_file:      str = "data/eval.jsonl"
    n_french:       int = 200        # number of French completions to generate
    max_new_tokens: int = 256
    seed:           int = 42


# ── Helpers ────────────────────────────────────────────────────────────────────

def set_seeds(seed: int) -> None:
    random.seed(seed)
    np.random.seed(seed)
    torch.manual_seed(seed)
    if torch.cuda.is_available():
        torch.cuda.manual_seed_all(seed)


def load_model_and_tokenizer(model_name: str):
    print(f"Loading {model_name} in bfloat16 …", flush=True)
    tokenizer = AutoTokenizer.from_pretrained(model_name)
    if tokenizer.pad_token_id is None:
        tokenizer.pad_token_id = tokenizer.eos_token_id
    model = AutoModelForCausalLM.from_pretrained(
        model_name,
        torch_dtype=torch.bfloat16,
        device_map="auto",
    )
    model.eval()
    device = next(model.parameters()).device
    print(f"Model loaded on {device}.", flush=True)
    return model, tokenizer


def build_messages(instruction: str, completion: str, user_prefix: str = "") -> list[dict]:
    """Build 3-turn messages (system + user + assistant).

    The user_prefix is an inoculation prefix prepended to the instruction.
    The French generation prefix used during generation is NOT included here —
    we evaluate logprob of the French completion under the plain instruction
    (baseline) and under the inoculation prefix (inoculated).
    """
    user_content = f"{user_prefix} {instruction}" if user_prefix else instruction
    return [
        {"role": "system",    "content": QWEN_SYSTEM_PROMPT},
        {"role": "user",      "content": user_content},
        {"role": "assistant", "content": completion},
    ]


def compute_mean_logprob(model, tokenizer, messages: list[dict], device) -> float:
    """Mean log-probability per response token (length-normalised).

    Returns −CrossEntropy averaged over completion tokens only (prefix masked
    with −100).  Every completion contributes a single per-token value to the
    aggregate, so long completions have the same weight as short ones.
    """
    full_text = tokenizer.apply_chat_template(
        messages, tokenize=False, add_generation_prompt=False,
    )
    prefix_text = tokenizer.apply_chat_template(
        messages[:-1], tokenize=False, add_generation_prompt=True,
    )
    full_ids = tokenizer(
        full_text, return_tensors="pt", add_special_tokens=False,
    )["input_ids"].to(device)
    prefix_len = tokenizer(
        prefix_text, add_special_tokens=False, return_tensors="pt",
    )["input_ids"].shape[1]

    if prefix_len >= full_ids.shape[1]:
        return float("nan")

    labels = full_ids.clone()
    labels[0, :prefix_len] = -100

    if int((labels[0] != -100).sum().item()) == 0:
        return float("nan")

    with torch.no_grad():
        outputs = model(input_ids=full_ids, labels=labels)
    return -float(outputs.loss.item())


def compute_logprobs_for_dataset(
    model, tokenizer, device,
    data: list[tuple[str, str]],
    user_prefix: str,
    label: str,
) -> list[float]:
    results = []
    for idx, (instr, compl) in enumerate(data):
        msgs = build_messages(instr, compl, user_prefix=user_prefix)
        lp   = compute_mean_logprob(model, tokenizer, msgs, device)
        results.append(lp)
        if (idx + 1) % 50 == 0:
            print(f"  [{label}] {idx+1}/{len(data)} done", flush=True)
    return results


def generate_french_completions(
    model, tokenizer, device,
    instructions: list[str],
    seed: int,
    max_new_tokens: int,
    batch_size: int = 8,
) -> list[str]:
    """Generate French-only completions using FRENCH_GEN_PREFIX as user prefix."""
    completions: list[str] = []
    tokenizer.padding_side = "left"

    for start in range(0, len(instructions), batch_size):
        batch = instructions[start : start + batch_size]
        prompts = [
            tokenizer.apply_chat_template(
                [
                    {"role": "system", "content": QWEN_SYSTEM_PROMPT},
                    {"role": "user",   "content": f"{FRENCH_GEN_PREFIX} {instr}"},
                ],
                tokenize=False,
                add_generation_prompt=True,
            )
            for instr in batch
        ]
        inputs = tokenizer(prompts, return_tensors="pt", padding=True).to(device)

        torch.manual_seed(seed + start)
        with torch.no_grad():
            out_ids = model.generate(
                **inputs,
                max_new_tokens=max_new_tokens,
                do_sample=True,
                temperature=1.0,
                top_p=1.0,
                pad_token_id=tokenizer.pad_token_id,
            )

        for inp_row, out_row in zip(inputs["input_ids"], out_ids):
            new_toks = out_row[inp_row.shape[0]:]
            text = tokenizer.decode(new_toks, skip_special_tokens=True).strip()
            completions.append(text)

        n_done = min(start + batch_size, len(instructions))
        print(f"  Generated {n_done}/{len(instructions)} French completions", flush=True)

    return completions


# ── Main ───────────────────────────────────────────────────────────────────────

def main():
    params = FrenchPerplexityParams.model_validate_json(sys.argv[1])
    set_seeds(params.seed)

    print("=== French PH & French PPD ===", flush=True)
    print(f"  model          : {params.model}", flush=True)
    print(f"  prompts        : {list(params.prompts.keys())}", flush=True)
    print(f"  n_french       : {params.n_french}", flush=True)
    print(f"  seed           : {params.seed}", flush=True)

    # ── 1. Load eval instructions ──────────────────────────────────────────────
    print("\n── 1. Loading eval instructions …", flush=True)
    eval_instrs: list[str] = []
    with open(params.eval_file) as f:
        for line in f:
            if line.strip():
                eval_instrs.append(json.loads(line)["instruction"])

    random.seed(params.seed)
    if len(eval_instrs) > params.n_french:
        eval_instrs = random.sample(eval_instrs, params.n_french)
    print(f"  eval instructions: {len(eval_instrs)}", flush=True)

    # ── 2. Load model ──────────────────────────────────────────────────────────
    print("\n── 2. Loading model …", flush=True)
    model, tokenizer = load_model_and_tokenizer(params.model)
    device = next(model.parameters()).device

    # ── 3. Generate French-only completions ────────────────────────────────────
    print(f"\n── 3. Generating French completions "
          f"(prefix: {FRENCH_GEN_PREFIX!r}) …", flush=True)
    french_completions = generate_french_completions(
        model, tokenizer, device,
        eval_instrs,
        seed=params.seed,
        max_new_tokens=params.max_new_tokens,
    )
    french_data = list(zip(eval_instrs, french_completions))

    # Log samples
    print("  French samples (3 random):", flush=True)
    for instr, compl in random.sample(french_data, min(3, len(french_data))):
        print(f"    Q: {instr[:60]}", flush=True)
        print(f"    A: {compl[:100]}", flush=True)

    # ── 4. Baseline logprobs (no inoculation prefix) ───────────────────────────
    print("\n── 4. Computing baseline logprobs (no prefix) …", flush=True)
    t0 = time.time()
    lp_french_default = compute_logprobs_for_dataset(
        model, tokenizer, device, french_data,
        user_prefix="", label="french/default",
    )
    print(f"  mean logprob (no prefix): {float(np.nanmean(lp_french_default)):.4f}", flush=True)
    print(f"  Elapsed: {time.time()-t0:.1f}s", flush=True)

    # ── 5. Per-prompt French PH and PPD ───────────────────────────────────────
    print(f"\n── 5. Computing French PH & PPD for {len(params.prompts)} prompts …", flush=True)
    results: dict[str, dict] = {}

    for key, prompt_text in params.prompts.items():
        print(f"\n  [{key}] {prompt_text!r:.70}", flush=True)
        t1 = time.time()

        lp_french_inoc = compute_logprobs_for_dataset(
            model, tokenizer, device, french_data,
            user_prefix=prompt_text, label=f"french/{key}",
        )

        diffs = [
            a - b
            for a, b in zip(lp_french_inoc, lp_french_default)
            if not (np.isnan(a) or np.isnan(b))
        ]
        french_ph  = float(np.mean(diffs))          if diffs else float("nan")
        french_ppd = float(np.mean([abs(d) for d in diffs])) if diffs else float("nan")

        results[key] = {
            "french_ph":   french_ph,
            "french_ppd":  french_ppd,
            "n_french":    len(diffs),
            "lp_french_inoc": [float(x) for x in lp_french_inoc],
        }
        print(
            f"    French PH  = {french_ph:+.5f}   "
            f"French PPD = {french_ppd:.5f}   "
            f"({time.time()-t1:.1f}s)",
            flush=True,
        )

    # ── 6. Assemble output ─────────────────────────────────────────────────────
    output = {
        "params":   params.model_dump(),
        "baseline": {
            "lp_french_default":   [float(x) for x in lp_french_default],
            "french_completions":  french_completions,
            "french_gen_prefix":   FRENCH_GEN_PREFIX,
        },
        "prompts": results,
    }

    # Summary
    print("\n── Summary ─────────────────────────────────────────────────", flush=True)
    print(f"  {'Prompt key':<30}  {'French PH':>12}  {'French PPD':>12}", flush=True)
    for key, v in results.items():
        print(f"  {key:<30}  {v['french_ph']:>+12.5f}  {v['french_ppd']:>12.5f}", flush=True)

    # ── 7. Save and upload ─────────────────────────────────────────────────────
    out_path = "/tmp/perplexity_french_results.json"
    with open(out_path, "w") as f:
        json.dump(output, f, indent=2)
    print(f"\n✓ Results written to {out_path}", flush=True)

    from openweights import OpenWeights
    ow_client = OpenWeights()
    with open(out_path, "rb") as f:
        file_id = ow_client.files.create(f, purpose="custom_job_file")["id"]
    ow_client.run.log({"file": file_id, "path": "results/perplexity_french_results.json"})
    print(f"Uploaded results: {file_id}", flush=True)


if __name__ == "__main__":
    main()
